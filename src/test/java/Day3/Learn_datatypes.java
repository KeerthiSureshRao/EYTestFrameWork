package Day3;

public class Learn_datatypes
{
    public static void main(String[] args) {
        //    Whole numbers
        byte byt = 127;
        System.out.println("Byte value : " + byt);
        short shot = 32767;
        System.out.println("Short value : " + shot);
        int init = 2147483647;
        System.out.println("Integer value : " + init);
        long lng = 9223372036854750000l;
        System.out.println("Long value : " + lng);
        //    Precisions
        float flt = 2.12345678901234567890f;
        System.out.println("Float value : " + flt);

        double dble = 2.12345678901234567890;
        System.out.println("Double Value : " + dble);
        // Character
        char Crctr = 'K' ;
        System.out.println("Character is : " + Crctr);
        // Boolean
        boolean bln = true ;
        System.out.println("Boolean is : " + bln);
    }
}
